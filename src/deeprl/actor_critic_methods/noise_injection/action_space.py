import torch
from torch import Tensor


class Gaussian:
    """https://en.wikipedia.org/wiki/Additive_white_Gaussian_noise"""

    def __init__(self, stdev: float) -> None:
        self.stdev = stdev

    def __call__(self, action: Tensor) -> Tensor:
        return torch.randn_like(action) * self.stdev


